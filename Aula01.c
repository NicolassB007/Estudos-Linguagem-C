// Variáveis
#include <stdio.h>

int main() { // A chave delimita um bloco de código (Incio do bloco)
    // Declarando variável
    int idade;

    // Função que imprime algo na tela
    printf("Ola, Mundo\n"); // O ponto e virgula finaliza um comando

    // Recebe os dados
    scanf("%d", &idade);

    printf("A sua idade é = %d", idade);

    return 0;
} // Fim do bloco